"""Склонение русских существительных при числительных.

Использование:
    from pluralize import plural

    plural(1, "перевод", "перевода", "переводов")   # → "перевод"
    plural(2, "перевод", "перевода", "переводов")   # → "перевода"
    plural(7, "перевод", "перевода", "переводов")   # → "переводов"
    f"{n} {plural(n, 'час', 'часа', 'часов')}"      # 1 час, 22 часа, 111 часов

Правило: форма зависит от последней цифры, кроме «подростков» 11–14,
которые всегда во множественной форме (11 переводов, 12 часов).
"""


def plural(n: int, one: str, few: str, many: str) -> str:
    """Возвращает форму слова для числа n: one=1, few=2–4, many=остальные."""
    if n % 10 == 1 and n % 100 != 11:
        return one
    if 2 <= n % 10 <= 4 and not 12 <= n % 100 <= 14:
        return few
    return many
